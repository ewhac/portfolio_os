FontEntry *
AddOneChar( int32 character, int32 width, CelData *image, int32 imagesize )
{
	FontEntry *fontentry;

/*???Superkprintf("AddOneChar( ");*/
/*???Superkprintf("character=%ld ", (unsigned long)(character));*/
/*???Superkprintf(" )\n");*/
 
	fontentry = (FontEntry *)SUPER_ALLOCMEM( sizeof(FontEntry), MEMTYPE_ANY );
	if ( fontentry == 0 ) return( 0 );

	fontentry->ft_CharValue = character;
	fontentry->ft_Width = width;
	fontentry->ft_Image = image;
	fontentry->ft_ImageByteCount = imagesize;

	/* Insert this character into the entry, and add it to the end of the 
	 * Least Recently Used list
	 */
	InsertFontEntry( fontentry );
	
/*???PrintTreeFont( GrafBase->gf_FontEntryHead->ft_GreaterBranch );*/
/*???Superkprintf("\n");*/
	return( fontentry );
}


int32
InitDefaultFont()
{
	int32 i;
	FontChar *fontchar;
	int32 retvalue;
	
	fontchar = FontArray;
	for ( i = 0; i < FONT_CHAR_COUNT; i++ )
		{
		if ( AddOneChar( fontchar->fc_CharValue, 
				fontchar->fc_Width, 
				fontchar->fc_Image, 
				3 * 4 * 8 ) == 0 )
			{
			retvalue = GRAFERR_NOMEM;
			goto DONE;
			}
		fontchar++;
		}
	
	DefaultFontEntryFont = GrafBase->gf_FontEntryHead->ft_GreaterBranch;

	DefaultFont.font_FontEntries = DefaultFontEntryFont;
	
/*???PrintTreeFont( GrafBase->gf_FontEntryHead->ft_GreaterBranch );*/
/*???Superkprintf("\n");*/

	retvalue = 0;

DONE:
	return( retvalue );
}


